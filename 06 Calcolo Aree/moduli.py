def quadrato():

	x = int (input ("Inserisci il Valore del lato:"))
	print ("Il valore dell'area del Quadrato è:", x**2)

def rettangolo():

	x = int (input ("Inserisci il Valore del lato:"))
	y = int (input ("Inserisci il Valore della base:"))
	print ("Il valore dell'area del rettangolo è:", x*y)

def triangolo():

	x = int (input ("Inserisci il valore della base:"))
	y = int (input ("Inserisci il valore dell'altezza:"))
	print ("Il valore dell'area del triangolo è:", (x*y) / 2)

